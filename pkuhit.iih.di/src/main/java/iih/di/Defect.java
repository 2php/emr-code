package iih.di;

public class Defect
{
    String name;

    String bugCd;

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getBugCd()
    {
        return bugCd;
    }

    public void setBugCd(String bugCd)
    {
        this.bugCd = bugCd;
    }
}